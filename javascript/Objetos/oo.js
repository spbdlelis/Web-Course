//Princípios Importantes
//1. Abstração -> traduzir objetos do mundo real para a programação com certa simplificação
//Ex: Para um sistema voltado ao Detran a propriedade Placa é mais importante que o método acelerar por exemplo
//
//2. Encapsulamento -> Detalhes de implementação são irrelevantes, somente a usabilidade é relevante
//Ex: Para dirigir um carro não importam detalhes de mecânica, apenas saber usar a "Interface"
//
//3. Herança (prototype)
//
//4. Polimorfismo -> A partir de um conceito tem-se objetos específicos
//Ex: um objeto do tipo celular pode ser um iphone, um ciaomi, etc